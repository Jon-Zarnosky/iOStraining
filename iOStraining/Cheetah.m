    self.habitat = @"commercials";
    self.diet = @"cheetos";
    
    self.numberArms = 0;
    self.numberFins = 0;
    self.numberLegs = 4;
    
    self.breathesAir = YES;
    self.warmBlooded = YES;
}

@end
